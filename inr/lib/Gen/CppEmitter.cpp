// REGISTER BACKEND

struct RegisterClassInfo : public InrGenInfo {
    gen_int Size;
    gen_list Regs;

    RegisterClassInfo(const Record* gpr) {
        const Init* SizeInit = gpr->getField("Size");
        const Init* RegsInit = gpr->getField("Regs");

        if(!SizeInit || !RegsInit) return;

        if(!SizeInit->matches(RecordType::Kind::Integer)) return;
        if(!RegsInit->matches(RecordType::Kind::List)) return;

        Size = IntegerInit::get(SizeInit);
        Regs = &ListInit::get(RegsInit);

        Success = true;
    }
};

struct RegisterInfo : public InrGenInfo {
    gen_string Name;
    gen_int Size;
    gen_list SubRegs;

    RegisterInfo(const Record* reg) {
        const Init* NameInit = reg->getField("Name");
        const Init* SizeInit = reg->getField("Size");
        const Init* SubRegsInit = reg->getField("SubRegs");

        if(!NameInit || !SizeInit || !SubRegsInit) return;

        if(!NameInit->matches(RecordType::Kind::String)) return;
        if(!SizeInit->matches(RecordType::Kind::Integer)) return;
        if(!SubRegsInit->matches(RecordType::Kind::List)) return;

        Name = StringInit::get(NameInit);
        Size = IntegerInit::get(SizeInit);
        SubRegs = &ListInit::get(SubRegsInit);

        Success = true;
    }
};

static inline TargetInfo getTargetInfo(const RecordStorage& result) {
    std::vector<const Record*> targetDef = result.getDefsDerivedFrom("Target");
    if(targetDef.empty() || targetDef.size() > 1) {
        vError("there must be only 1 target def");
        return {};
    }

    return {targetDef.front()};
}

bool RegisterBackend::emit(const RecordStorage& result) {
    TargetInfo target = getTargetInfo(result);
    if(!target.Success) return true;

    std::vector<const Record*> registers =
        result.getDefsDerivedFrom("Register");
    std::vector<const Record*> registerClasses =
        result.getDefsDerivedFrom("RegisterClass");

    addComment("Generated by inr-gen, backend: Register");

    bool err = false;

    constexpr sview REGISTER_HEADER_MACRO = "_REGISTER_HEADER";

    // REGISTER HEADER
    addIfDef(target.Namespace, REGISTER_HEADER_MACRO);
    addUndef(target.Namespace, REGISTER_HEADER_MACRO);

    // NAMESPACE
    addNamespace(target.Namespace);
    openBody();

    struct LocalRegInfo {
        size_t idx;
        size_t strIdx;
        size_t subregIdx;
        size_t subregC;
    };

    struct LocalRegClassInfo {
        size_t idx;
        size_t strIdx;
    };

    std::unordered_map<const Record*, LocalRegInfo> mapOfRegisters;
    std::unordered_map<const Record*, LocalRegClassInfo> mapOfRegClasses;

    write("constexpr const char* ", target.Namespace, "_StrTable = \"");

    size_t currentStrIdx = 0;
    for(const Record* reg : registers) {
        RegisterInfo info(reg);
        if(!info.Success) {
            err = true;
            break;
        }
        mapOfRegisters[reg].strIdx = currentStrIdx;
        write(info.Name, "\\0");
        currentStrIdx += info.Name.size() + 1;
    }

    for(const Record* regclass : registerClasses) {
        mapOfRegClasses[regclass].strIdx = currentStrIdx;
        write(regclass->getName(), "\\0");
        currentStrIdx += regclass->getName().size() + 1;
    }

    writeln("\";");

    write("constexpr Register ", target.Namespace, "_RegsDef[] = ");
    openBody();
    size_t currentIdx = 0;

    for(const Record* reg : registers) {
        mapOfRegisters[reg].idx = currentIdx;
        write("\tRegister::createPhysical(", currentIdx++, "), ");
        addComment(reg->getName());
    }

    for(const Record* reg : registers) {
        RegisterInfo info(reg);
        if(!info.Success) {
            err = true;
            break;
        }

        LocalRegInfo& lreginfo = mapOfRegisters[reg];
        if(info.SubRegs->empty()) {
            lreginfo.subregIdx = 0;
            lreginfo.subregC = 0;
            continue;
        }
        write('\t');
        addComment("Subregisters of ", reg->getName());
        lreginfo.subregIdx = currentIdx;
        lreginfo.subregC = info.SubRegs->size();

        for(const Init* subregInit : *info.SubRegs) {
            const Record* subReg = DefInit::get(subregInit);
            write("\tRegister::createPhysical(", mapOfRegisters[subReg].idx,
                  "), ");
            addComment(subReg->getName());
        }

        currentIdx += info.SubRegs->size();
    }
    for(const Record* regclass : registerClasses) {
        RegisterClassInfo info(regclass);
        if(!info.Success) {
            err = true;
            break;
        }
        mapOfRegClasses[regclass].idx = currentIdx;

        write('\t');
        addComment("Register class ", regclass->getName());

        for(const Init* regInit : *info.Regs) {
            const Record* reg = DefInit::get(regInit);
            write("\tRegister::createPhysical(", mapOfRegisters[reg].idx,
                  "), ");
            addComment(reg->getName());
        }

        currentIdx += info.Regs->size();
    }

    closeBody(true);

    for(size_t i = 0; i < registers.size(); i++) {
        writeln("constexpr const Register& ", registers[i]->getName(), " = ",
                target.Namespace, "_RegsDef[", i, "];");
    }

    write("constexpr RegisterDesc ", target.Namespace, "_RegsDesc[] = ");
    openBody();

    for(const Record* reg : registers) {
        LocalRegInfo& lreginfo = mapOfRegisters[reg];
        write("\t{", lreginfo.strIdx, ", ", lreginfo.subregIdx, ", ",
              lreginfo.subregC, "},\n");
    }

    closeBody(true);

    write("constexpr RegisterClass ", target.Namespace, "_RegsClass[] = ");
    openBody();

    for(const Record* regclass : registerClasses) {
        RegisterClassInfo info(regclass);
        if(!info.Success) {
            err = true;
            break;
        }
        write("\t{", mapOfRegClasses[regclass].idx, ", ", info.Regs->size(),
              ", ", info.Size, ", ", mapOfRegClasses[regclass].strIdx, "},\n");
    }

    closeBody(true);

    for(size_t i = 0; i < registerClasses.size(); i++) {
        writeln("constexpr const RegisterClass& ",
                registerClasses[i]->getName(), " = ", target.Namespace,
                "_RegsClass[", i, "];");
    }

    writeln("constexpr RegisterInfo RegInfo = {", target.Namespace, "_StrTable",
            ", ", target.Namespace, "_RegsDef", ", ", registers.size(), ", ",
            target.Namespace, "_RegsDesc", ", ", target.Namespace, "_RegsClass",
            "};");

    closeBody();
    // END OF NAMESPACE

    addEndIf();
    // END OF REGISTER HEADER

    return err;
}

// CALLING CONV BACKEND

bool CallingConvBackend::emit(const RecordStorage& result) {
    TargetInfo target = getTargetInfo(result);
    if(!target.Success) return true;

    std::vector<const Record*> convs = result.getDefsDerivedFrom("CallingConv");

    addComment("Generated by inr-gen, backend: Calling Convention");

    bool err = false;

    constexpr sview CALLING_CONV_HEADER_MACRO = "_CALLING_CONV_HEADER";

    // CALLING CONV HEADER
    addIfDef(target.Namespace, CALLING_CONV_HEADER_MACRO);
    addUndef(target.Namespace, CALLING_CONV_HEADER_MACRO);

    // NAMESPACE
    addNamespace(target.Namespace);
    openBody();

    closeBody();
    // END OF NAMESPACE

    addEndIf();
    // END OF CALLING CONV HEADER

    return err;
}

