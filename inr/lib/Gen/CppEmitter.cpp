// REGISTER BACKEND

struct RegisterClassInfo : public InrGenInfo {
    gen_int Size;
    gen_list Regs;

    RegisterClassInfo(const Record* gpr) {
        const Init* SizeInit = gpr->getField("Size");
        const Init* RegsInit = gpr->getField("Regs");

        if(!SizeInit || !RegsInit) return;

        if(!SizeInit->matches(RecordType::Kind::Integer)) return;
        if(!RegsInit->matches(RecordType::Kind::List)) return;

        Size = IntegerInit::get(SizeInit);
        Regs = &ListInit::get(RegsInit);

        Success = true;
    }
};

struct RegisterInfo : public InrGenInfo {
    gen_string Name;
    gen_int Size;
    gen_list SubRegs;

    RegisterInfo(const Record* reg) {
        const Init* NameInit = reg->getField("Name");
        const Init* SizeInit = reg->getField("Size");
        const Init* SubRegsInit = reg->getField("SubRegs");

        if(!NameInit || !SizeInit || !SubRegsInit) return;

        if(!NameInit->matches(RecordType::Kind::String)) return;
        if(!SizeInit->matches(RecordType::Kind::Integer)) return;
        if(!SubRegsInit->matches(RecordType::Kind::List)) return;

        Name = StringInit::get(NameInit);
        Size = IntegerInit::get(SizeInit);
        SubRegs = &ListInit::get(SubRegsInit);

        Success = true;
    }
};

static inline TargetInfo getTargetInfo(const RecordStorage& result) {
    std::vector<const Record*> targetDef = result.getDefsDerivedFrom("Target");
    if(targetDef.empty() || targetDef.size() > 1) {
        vError("there must be only 1 target def");
        return {};
    }

    return {targetDef.front()};
}

bool RegisterBackend::emit(const RecordStorage& result) {
    TargetInfo target = getTargetInfo(result);
    if(!target.Success) return true;

    std::vector<const Record*> registers =
        result.getDefsDerivedFrom("Register");
    std::vector<const Record*> registerClasses =
        result.getDefsDerivedFrom("RegisterClass");

    addComment("Generated by inr-gen, backend: Register");

    bool err = false;

    constexpr sview REGISTER_HEADER_MACRO = "_REGISTER_HEADER";

    // REGISTER HEADER
    addIfDef(target.Namespace, REGISTER_HEADER_MACRO);
    addUndef(target.Namespace, REGISTER_HEADER_MACRO);

    // NAMESPACE
    addNamespace(target.Namespace);
    openBody();

    struct LocalRegInfo {
        size_t idx;
        size_t strIdx;
        size_t subregIdx;
        size_t subregC;
        size_t superRegIdx;
        std::vector<const Record*> superRegs;
    };

    struct LocalRegClassInfo {
        size_t idx;
        size_t strIdx;
    };

    std::unordered_map<const Record*, LocalRegInfo> mapOfRegisters;
    std::unordered_map<const Record*, LocalRegClassInfo> mapOfRegClasses;

    write("constexpr const char* ", target.Namespace, "_StrTable = \"");

    size_t currentStrIdx = 0;
    for(const Record* reg : registers) {
        RegisterInfo info(reg);
        if(!info.Success) {
            err = true;
            break;
        }
        mapOfRegisters[reg].strIdx = currentStrIdx;
        write(info.Name, "\\0");
        currentStrIdx += info.Name.size() + 1;
    }

    for(const Record* regclass : registerClasses) {
        mapOfRegClasses[regclass].strIdx = currentStrIdx;
        write(regclass->getName(), "\\0");
        currentStrIdx += regclass->getName().size() + 1;
    }

    writeln("\";");

    write("constexpr Register ", target.Namespace, "_RegsDef[] = ");
    openBody();
    size_t currentIdx = 0;

    for(const Record* reg : registers) {
        mapOfRegisters[reg].idx = currentIdx;
        write("\tRegister::createPhysical(", currentIdx++, "), ");
        addComment(reg->getName());
    }

    for(const Record* reg : registers) {
        RegisterInfo info(reg);
        if(!info.Success) {
            err = true;
            break;
        }

        LocalRegInfo& lreginfo = mapOfRegisters[reg];
        if(info.SubRegs->empty()) {
            lreginfo.subregIdx = 0;
            lreginfo.subregC = 0;
            continue;
        }
        write('\t');
        addComment("Subregisters of ", reg->getName());
        lreginfo.subregIdx = currentIdx;
        lreginfo.subregC = info.SubRegs->size();

        for(const Init* subregInit : *info.SubRegs) {
            const Record* subReg = DefInit::get(subregInit);
            LocalRegInfo& lsubreginfo = mapOfRegisters[subReg];
            lsubreginfo.superRegs.emplace_back(reg);
            write("\tRegister::createPhysical(", lsubreginfo.idx, "), ");
            addComment(subReg->getName());
        }

        currentIdx += info.SubRegs->size();
    }
    for(const Record* regclass : registerClasses) {
        RegisterClassInfo info(regclass);
        if(!info.Success) {
            err = true;
            break;
        }
        mapOfRegClasses[regclass].idx = currentIdx;

        write('\t');
        addComment("Register class ", regclass->getName());

        for(const Init* regInit : *info.Regs) {
            const Record* reg = DefInit::get(regInit);
            write("\tRegister::createPhysical(", mapOfRegisters[reg].idx,
                  "), ");
            addComment(reg->getName());
        }

        currentIdx += info.Regs->size();
    }

    for(const Record* reg : registers) {
        RegisterInfo info(reg);
        if(!info.Success) {
            err = true;
            break;
        }

        LocalRegInfo& lreginfo = mapOfRegisters[reg];
        if(lreginfo.superRegs.empty()) {
            lreginfo.superRegIdx = 0;
            continue;
        }
        lreginfo.superRegIdx = currentIdx;

        write('\t');
        addComment("Superregs of ", reg->getName());

        for(const Record* superreg : lreginfo.superRegs) {
            write("\tRegister::createPhysical(", mapOfRegisters[superreg].idx,
                  "), ");
            addComment(superreg->getName());
        }

        currentIdx += lreginfo.superRegs.size();
    }

    closeBody(true);

    for(size_t i = 0; i < registers.size(); i++) {
        writeln("constexpr const Register& ", registers[i]->getName(), " = ",
                target.Namespace, "_RegsDef[", i, "];");
    }

    write("constexpr RegisterDesc ", target.Namespace, "_RegsDesc[] = ");
    openBody();

    for(const Record* reg : registers) {
        LocalRegInfo& lreginfo = mapOfRegisters[reg];
        write("\t{", lreginfo.strIdx, ", ", lreginfo.subregIdx, ", ",
              lreginfo.superRegIdx, ", ", lreginfo.subregC, ", ",
              lreginfo.superRegs.size(), "},\n");
    }

    closeBody(true);

    write("constexpr RegisterClass ", target.Namespace, "_RegsClass[] = ");
    openBody();

    for(const Record* regclass : registerClasses) {
        RegisterClassInfo info(regclass);
        if(!info.Success) {
            err = true;
            break;
        }
        write("\t{", mapOfRegClasses[regclass].idx, ", ", info.Regs->size(),
              ", ", info.Size, ", ", mapOfRegClasses[regclass].strIdx, "},\n");
    }

    closeBody(true);

    for(size_t i = 0; i < registerClasses.size(); i++) {
        writeln("constexpr const RegisterClass& ",
                registerClasses[i]->getName(), " = ", target.Namespace,
                "_RegsClass[", i, "];");
    }

    writeln("constexpr RegisterInfo RegInfo = {", target.Namespace, "_StrTable",
            ", ", target.Namespace, "_RegsDef", ", ", registers.size(), ", ",
            target.Namespace, "_RegsDesc", ", ", target.Namespace, "_RegsClass",
            "};");

    closeBody();
    // END OF NAMESPACE

    addEndIf();
    // END OF REGISTER HEADER

    return err;
}

// CALLING CONV BACKEND

static inline void typeCondition(raw_stream& os, const Type* t) {
    os << "";
    switch(t->getTypeID()) {
        case Type::TypeID::Void:
            // Should be allowed?
            os << "type->isVoid()";
            break;
        case Type::TypeID::Integer:
            os << "(type->isInteger() && ((const "
                  "IntegerType*)type)->getWidth() "
                  "== "
               << ((const IntegerType*)t)->getWidth() << ')';
            break;
        case Type::TypeID::Pointer:
            os << "type->isPointer()";
            break;
        case Type::TypeID::Block:
            // Pointer to block?
            os << "type->isBlock()";
            break;
        case Type::TypeID::Function:
            // Not allowed
            break;
    }
}

static inline bool emitAction(raw_stream& os, const Record* action,
                              const RecordStorage& result) {
    const Record* CCAssignToReg = result.findClass("CCAssignToReg");
    const Record* CCAssignToStack = result.findClass("CCAssignToStack");
    (void)CCAssignToStack;
    const Record* CCPredicate = result.findClass("CCPredicate");
    const Record* CCIfType = result.findClass("CCIfType");

    if(action->isDerived(CCPredicate)) {
        const Record* subAction = DefInit::get(action->getField("SubAction"));

        if(action->isDerived(CCIfType)) {
            const Init* InitTypes = action->getField("Types");
            TargetInfo::gen_list ListTypes = &ListInit::get(InitTypes);

            os << "if(";
            for(const Init* ListTInit : *ListTypes) {
                if(ListTInit != ListTypes->front()) {
                    os << " || ";
                }
                os << '(';
                typeCondition(os, IRTypeInit::get(ListTInit));
                os << ')';
            }
            os << "){\n";
            if(emitAction(os, subAction, result)) return true;
            os << "}\n";
        }
    }
    else {
        if(action->isDerived(CCAssignToReg)) {
            TargetInfo::gen_list Regs =
                &ListInit::get(action->getField("Regs"));
            os << "Register reg = state.allocateReg({";
            for(const Init* InitReg : *Regs) {
                if(InitReg != Regs->front()) {
                    os << ", ";
                }
                os << DefInit::get(InitReg)->getName();
            }

            os << "});\n"
               << "if(!reg.isNone()) {\n"
               << "state.addAssign(CCAssign::getReg(valN, type, reg, type, "
                  "CCAssign::Info::Full));\n"
               << "return true;\n"
               << "}\n";
        }
    }

    return false;
}

static inline bool emitCC(raw_stream& os, const Record* cc,
                          const RecordStorage& result) {
    const Init* InitActions = cc->getField("Actions");
    if(!InitActions || !InitActions->matches(RecordType::Kind::List))
        return true;

    TargetInfo::gen_list Actions = &ListInit::get(InitActions);

    for(const Init* InitAction : *Actions) {
        if(emitAction(os, DefInit::get(InitAction), result)) {
            return true;
        }
    }

    return false;
}

bool CallingConvBackend::emit(const RecordStorage& result) {
    TargetInfo target = getTargetInfo(result);
    if(!target.Success) return true;

    std::vector<const Record*> convs = result.getDefsDerivedFrom("CallingConv");

    addComment("Generated by inr-gen, backend: Calling Convention");

    bool err = false;

    constexpr sview CALLING_CONV_HEADER_MACRO = "_CALLING_CONV_HEADER";

    // CALLING CONV HEADER
    addIfDef(target.Namespace, CALLING_CONV_HEADER_MACRO);
    addUndef(target.Namespace, CALLING_CONV_HEADER_MACRO);

    // NAMESPACE
    addNamespace(target.Namespace);
    openBody();

    for(const Record* cc : convs) {
        write("bool CC", cc->getName(),
              "(unsigned valN, const Type* type, CCState& state)");
        openBody();
        if(emitCC(os_, cc, result)) {
            err = true;
            break;
        }
        writeln("return false;");
        closeBody();
    }

    closeBody();
    // END OF NAMESPACE

    addEndIf();
    // END OF CALLING CONV HEADER

    return err;
}

using VarMap =
    std::unordered_map<const std::pair<const Init*, const StringInit*>*,
                       uint32_t>;
using OutVarMap = std::unordered_map<sview, uint32_t>;

static inline void mapDagOps(raw_stream& os, VarMap& map, const DagInit* in,
                             uint32_t& idx, const std::string& checkingOf,
                             uint32_t instIdx) {
    // os << "\t\tif(" << checkingOf << "->getNumOperands() != " <<
    // in->getArgs().size() << ") goto PATTERN_MATCH_NEXT_" << instIdx << ";\n";
    uint32_t opIdx = 0;
    for(const std::pair<const Init*, const StringInit*>& arg : in->getArgs()) {
        map[&arg] = idx;
        std::string name = "op";
        name += std::to_string(idx++);
        os << "\t\tDAGNode* " << name << " = " << checkingOf << "->getOperand("
           << opIdx++ << ");\n";
        if(arg.first->matches(RecordType::Kind::Dag)) {
            mapDagOps(os, map, (const DagInit*)arg.first, idx, name, instIdx);
        }
    }
}

static inline void emitDagIn(
    raw_stream& os, const DagInit* in,
    const std::unordered_map<const Record*, std::string>& dagOps,
    const RecordStorage& result, uint32_t instIdx, VarMap& varMap,
    const std::string& checkingOf) {
    auto op = dagOps.find(in->getOperator()->getValue());
    if(op == dagOps.end()) return;

    os << "\t\tif(" << checkingOf << "->getNodeType() == " << op->second
       << ") {\n";

    for(const std::pair<const Init*, const StringInit*>& arg : in->getArgs()) {
        if(arg.first->matches(RecordType::Kind::Dag)) {
            std::string name = "op";
            name += std::to_string(varMap[&arg]);
            emitDagIn(os, (const DagInit*)arg.first, dagOps, result, instIdx,
                      varMap, name);
        }
        else {
            // def
            const Record* rec = DefInit::get(arg.first);

            bool isImm = (rec == result.findDef("imm"));
            bool isReg = rec->isDerived(result.findClass("RegisterClass"));

            os << "\t\t\tif(!op" << varMap[&arg];

            if(isImm) {
                os << "->isImm()";
            }

            if(isReg) {
                os << "->isReg()";
            }

            os << ") {\n\t\t\t\tgoto PATTERN_MATCH_NEXT_" << instIdx
               << ";\n\t\t\t}\n";

            if(isReg) {
                os << "\t\t\telse {\n";

                os << "\t\t\t\tRegister reg = ((const DAGRegister*)op"
                   << varMap[&arg] << ")->getRegister();\n";
                os << "\t\t\t\tif(!regInfo->inRegClass(regInfo->getRegClass(\""
                   << rec->getName() << "\"), reg)) goto PATTERN_MATCH_NEXT_"
                   << instIdx << ";\n";

                os << "\t\t\t}\n";
            }
        }
    }

    os << "\t\t}\n\t\telse goto PATTERN_MATCH_NEXT_" << instIdx << ";\n";
}

static inline void emitDagOut(
    raw_stream& os, const DagInit* out,
    const std::unordered_map<const Record*, std::string>& dagOps,
    const RecordStorage& result, OutVarMap& varMap) {
    for(const std::pair<const Init*, const StringInit*>& arg : out->getArgs()) {
        if(arg.first->matches(RecordType::Kind::Dag)) {
            emitDagOut(os, (const DagInit*)arg.first, dagOps, result, varMap);
        }
        else {
            if(!arg.second) {
                vError("output dag MUST have a name binded to the args");
                return;
            }

            const Record* rec = DefInit::get(arg.first);
            sview argName = arg.second->getValue();

            bool isImm = (rec == result.findDef("imm"));
            bool isReg = rec->isDerived(result.findClass("RegisterClass"));

            os << "\t\t\tMachineOperand::";
            if(isImm) {
                os << "createImm(((const DAGConst*)op" << varMap[argName]
                   << ")->getValue())";
            }

            if(isReg) {
                os << "createReg(((const DAGRegister*)op" << varMap[argName]
                   << ")->getRegister())";
            }

            if(&arg != &out->getArgs().back()) {
                os << ',';
            }

            os << '\n';
        }
    }
}

static inline void emitDag(
    raw_stream& os, const Record* inst,
    const std::unordered_map<const Record*, std::string>& dagOps,
    const RecordStorage& result, uint32_t instIdx) {
    const DagInit* in = (const DagInit*)inst->getField("In");
    const DagInit* out = (const DagInit*)inst->getField("Out");

    os << "\t{\n";

    VarMap varMap;
    uint32_t idx = 0;
    mapDagOps(os, varMap, in, idx, "dag", instIdx);
    emitDagIn(os, in, dagOps, result, instIdx, varMap, "dag");

    auto op = dagOps.find(out->getOperator()->getValue());
    if(op == dagOps.end()) return;

    OutVarMap outVarMap;

    for(const std::pair<const std::pair<const Init*, const StringInit*>*,
                        uint32_t>
            conv : varMap) {
        if(conv.first->second) {
            outVarMap[conv.first->second->getValue()] = conv.second;
        }
    }

    os << "\t\tMachineInst::create(";

    os << op->second << ", block, {\n";
    emitDagOut(os, out, dagOps, result, outVarMap);

    os << "\t\t});\n\t\treturn;\n";

    os << "\t}\n";
}

bool ISelBackend::emit(const RecordStorage& result) {
    TargetInfo target = getTargetInfo(result);
    if(!target.Success) return true;

    std::unordered_map<const Record*, std::string> dagOps;

    dagOps[result.findDef("ADD")] = "(uint32_t)DAGType::ADD";
    dagOps[result.findDef("SUB")] = "(uint32_t)DAGType::SUB";
    dagOps[result.findDef("MUL")] = "(uint32_t)DAGType::MUL";
    dagOps[result.findDef("SDIV")] = "(uint32_t)DAGType::SDIV";
    dagOps[result.findDef("UDIV")] = "(uint32_t)DAGType::UDIV";
    dagOps[result.findDef("SREM")] = "(uint32_t)DAGType::SREM";
    dagOps[result.findDef("UREM")] = "(uint32_t)DAGType::UREM";
    dagOps[result.findDef("CopyToReg")] = "(uint32_t)DAGType::CopyToReg";
    dagOps[result.findDef("CopyFromReg")] = "(uint32_t)DAGType::CopyFromReg";

    std::vector<const Record*> instructions =
        result.getDefsDerivedFrom("Instruction");

    addComment("Generated by inr-gen, backend: ISel");

    bool err = false;

    // NAMESPACE
    addNamespace(target.Namespace);
    openBody();

    addIfDef(target.Namespace, "_NEED_OPCODES");
    addUndef(target.Namespace, "_NEED_OPCODES");

    write("enum class Opcodes : uint32_t ");
    openBody();
    writeln("\tOPCODE_START = (uint32_t)DAGType::DAG_TYPE_END,");

    uint32_t idx = 1;
    for(const Record* inst : instructions) {
        std::string str = "(uint32_t)Opcodes::";
        str += inst->getName();
        dagOps[inst] = std::move(str);
        writeln('\t', inst->getName(), ',');
    }

    closeBody(true);

    addEndIf();

    addIfDef(target.Namespace, "_NEED_STRTABLE");
    addUndef(target.Namespace, "_NEED_STRTABLE");

    write("constexpr const char* OpcodeAsmStr[] = ");
    openBody();

    for(const Record* inst : instructions) {
        sview asmStr = StringInit::get(inst->getField("AsmStr"));
        writeln("\t\"", asmStr, "\",");
    }

    closeBody(true);

    addEndIf();

    addIfDef(target.Namespace, "_NEED_ISEL");
    addUndef(target.Namespace, "_NEED_ISEL");

    write("void ", target.Namespace,
          "matchEmit(DAGNode* dag, MachineBlock* block, const RegisterInfo* "
          "regInfo) ");
    openBody();

    idx = 0;
    for(const Record* inst : instructions) {
        emitDag(os_, inst, dagOps, result, idx);
        writeln("PATTERN_MATCH_NEXT_", idx++, ':');
    }

    writeln("\treturn;");

    closeBody();

    addEndIf();

    closeBody();
    // END OF NAMESPACE

    return err;
}

